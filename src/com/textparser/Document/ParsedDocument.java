package com.textparser.Document;

import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author catalin.dumitru
 */
public class ParsedDocument {
    protected String _fullText;
    protected String _name;
    protected String _path;
    
    protected List<ParsedSentence> _sentances;

    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public ParsedDocument() {
        this._sentances = new ArrayList<ParsedSentence>();
        this._fullText = "";
        this._name = "";
        this._path = "";
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public ParsedDocument(List<ParsedSentence> sentances) {
        this._sentances = sentances;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public String fullText() {
        return this._fullText;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public String name() {
        return this._name;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public String path() {
        return this._path;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public List<ParsedSentence> sentances() {
        return this._sentances;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setFullText(String text) {
        this._fullText = text;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    //unused???
    public void setSentances(List<ParsedSentence> sentances) {
        this._sentances = sentances;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void addSentance(ParsedSentence what) {
        this._sentances.add(what);                
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void clearSentences() {
        this._sentances.clear();
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setName(String name) {
        this._name = name;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public void setPath(String path) {
        this._path = path;
    }


}
